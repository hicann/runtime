while true; do
    case "$1" in
    --install-path=*)
        install_path=$(echo "$1" | cut -d"=" -f2-)
        shift
        ;;
    --version-dir=*)
        version_dir=$(echo "$1" | cut -d"=" -f2-)
        shift
        ;;
    *)
        break
        ;;
    esac
done

sed -i "s#version_dirpath=.\+#version_dirpath=\"${install_path}/${version_dir}\"#" "${install_path}/${version_dir}/set_env.sh"
