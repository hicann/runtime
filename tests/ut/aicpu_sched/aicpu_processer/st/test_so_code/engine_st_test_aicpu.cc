typedef struct
{
    uint32_t optype;
}STR_TF_KERNEL;

extern "C" uint32_t TFOperateAPI(const void * param_base)
{
  STR_TF_KERNEL *param = (STR_TF_KERNEL *)param_base;
  param->optype ++;

  printf("Call TFOperateAPI && set optype to %d\n", param->optype);

  if (param->optype == 0x3840)
    return 1;

  return 0;
}

