TEST_F(ELFTest, ELF_Process_Object_13)
{
    size_t MAX_LENGTH = 75776;
    FILE *bin = nullptr;

    bin = fopen("llt/ace/npuruntime/runtime/ut/runtime/test/data/no-kernel.o", "rb");
    if (bin == nullptr) {
        printf("error\n");
        return;
    } else {
        printf("succ\n");
    }

    char bindata[MAX_LENGTH];
    fread(bindata, sizeof(char), MAX_LENGTH, bin);
    fclose(bin);

    rtElfData *elfData;
    RtKernel *kernels;

    elfData = new rtElfData;

    bool isSupportMix = false;
    kernels = ProcessObject(bindata, elfData, 0, &isSupportMix);
    if (nullptr == kernels) {
        printf("SUCC no kernel!\n");
    } else {
        printf("FAIL\n");
    }

    if (nullptr != elfData->section_headers) {
        delete[] elfData->section_headers;
        elfData->section_headers = nullptr;
    }

    uint8_t buf[64] = {0};
    uint8_t *bufPtr = buf;
    uint32_t totalLen = sizeof(ElfFuncTypeInfo) + sizeof(ElfKernelSyncInfo) + sizeof(ElfKernelAivTypeInfo) +
                        sizeof(ElfKernelReportSzInfo);
    ElfFuncTypeInfo *typeInfo = (ElfFuncTypeInfo *)bufPtr;
    typeInfo->head.type = FUNC_META_TYPE_KERNEL_TYPE;
    typeInfo->head.length = 4;
    typeInfo->funcType = KERNEL_FUNCTION_TYPE_AICORE;

    bufPtr = bufPtr + sizeof(ElfFuncTypeInfo);

    ElfKernelSyncInfo *syncInfo = (ElfKernelSyncInfo *)bufPtr;
    syncInfo->head.type = FUNC_META_TYPE_CROSS_CORE_TYPE;
    syncInfo->head.length = 4;
    syncInfo->crossCoreSync = FUNC_USE_SYNC;

    bufPtr = bufPtr + sizeof(ElfKernelAivTypeInfo);
    ElfKernelAivTypeInfo *aivTypeInfo = (ElfKernelAivTypeInfo *)bufPtr;
    aivTypeInfo->head.type = FUNC_META_TYPE_AIV_TYPE_FLAG;
    aivTypeInfo->head.length = 4;
    aivTypeInfo->aivType = static_cast<uint32_t>(AivTypeFlag::AIV_TYPE_SIMT_VF_ONLY);

    bufPtr = bufPtr + sizeof(ElfKernelReportSzInfo);
    ElfKernelReportSzInfo *reportSzInfo = (ElfKernelReportSzInfo *)bufPtr;
    reportSzInfo->head.type = FUNC_META_TYPE_COMPILER_ALLOC_UB_SIZE;
    reportSzInfo->head.length = 4;
    reportSzInfo->shareMemSize = 2048U;

    ElfKernelInfo kernelInfo = {0, 0};

    GetKernelTlvInfo(buf, 0, &kernelInfo);

    EXPECT_EQ(kernelInfo.funcType, 0);
    EXPECT_EQ(kernelInfo.crossCoreSync, 0);

    GetKernelTlvInfo(buf, totalLen, &kernelInfo);

    EXPECT_EQ(kernelInfo.funcType, KERNEL_FUNCTION_TYPE_AICORE);
    EXPECT_EQ(kernelInfo.crossCoreSync, FUNC_USE_SYNC);

    EXPECT_EQ(kernelInfo.kernelVfType, static_cast<uint32_t>(AivTypeFlag::AIV_TYPE_SIMT_VF_ONLY));
    EXPECT_EQ(kernelInfo.shareMemSize, 2048U);

    delete elfData;
    elfData = nullptr;
    delete[] kernels;
    kernels = nullptr;
}

